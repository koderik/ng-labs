    public static void writeCars() {
        txaWorkshop.setText("");
        for (int i = 0; i < CarList.size(); i++) {
            txaWorkshop.append(CarList.get(i).toString() + "\n");

        }
    }

    public static int plateSearch(String plate) {

        for (int i = 0; i < CarList.size(); i++) {
            if (plate.equalsIgnoreCase(CarList.get(i).getPlate())) {
                return i;

            }
        }
        return -1;
    }
    public static void makeSort(){
        for (int i = 0; i < CarList.size(); i++) {
            int minIndex = i;
            for (int j = i + 1; j < CarList.size(); j++) {
                if (CarList.get(minIndex).getMake().compareToIgnoreCase(CarList.get(j).getMake()) > 0) {
                    minIndex = j;
                }
            }
            Car temp = CarList.get(minIndex);
            CarList.set(minIndex, CarList.get(i));
            CarList.set(i, temp);
        }
    }
    public static void yearSort(){
        for (int i = 0; i < CarList.size(); i++) {
            int temp = CarList.get(i).getYear();
            Car tempCar = CarList.get(i);
            int j = i - 1;
            while (j >= 0 && CarList.get(j).getYear() > temp) {
                CarList.set(j + 1, CarList.get(j));
                j--;
            }
            CarList.set(j + 1, tempCar);
        }
    }
    public static int yearSearch(String year){
        yearSort();
        int x = Integer.valueOf(year);
        int begin = 0;
        int last = CarList.size();
        int mid = 0;
        while(begin<=last){
            mid = (begin+last)/2;
            if (CarList.get(mid).getYear()<x) {
                begin = mid+1;
            }
            else if(CarList.get(mid).getYear()>x){
                last = mid-1;
            }
            else{
                return mid;
            }
        }
        return -1;
    }
