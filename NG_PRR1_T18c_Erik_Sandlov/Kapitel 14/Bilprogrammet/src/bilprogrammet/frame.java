    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String key = this.txfSearch.getText();

        if (this.rbtnPlate.isSelected()) {
            int index = plateSearch(key);
            if (index == -1) {
                this.txfResult.setText("Ingen bil innehåller regnr " + key);
            } else {
                this.txfResult.setText("Bilen med regnr " + key + " är en " + CarList.get(index).getMake());
            }
        }
        else if(this.rbtnYear.isSelected()){
           
            int index = yearSearch(key);
            if (index == -1) {
                this.txfResult.setText("Ingen bil har årtalet " + key);
            } else {
                this.txfResult.setText("Första bilen med årtalet " + key + " är en " + CarList.get(index).getMake());
            }
        }
        else{
            this.txfResult.setText("Search for make or model by selecting the corresponding button above");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

