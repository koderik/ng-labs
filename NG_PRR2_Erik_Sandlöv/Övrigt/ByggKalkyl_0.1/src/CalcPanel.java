    private void jcbConstructionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbConstructionActionPerformed
        // TODO add your handling code here:
        if (this.jcbConstruction.getSelectedIndex() == 0) {
            this.jtf0.setVisible(false);
            this.jtf1.setVisible(false);
            this.jl0.setVisible(false);
            this.jl1.setVisible(false);
            this.jButtonOk.setVisible(false);
            this.jtaCList.setText("");
            this.jtaCList.setVisible(false);
        } else if (this.jcbConstruction.getSelectedIndex() == 1) {
            this.jtf0.setVisible(true);
            this.jtf1.setVisible(true);
            this.jl0.setText("Trappsteg");
            this.jl0.setVisible(true);
            this.jl1.setText("Bredd(mm)");
            this.jl1.setVisible(true);
            this.jButtonOk.setVisible(true);
            this.jtaCList.setText("");
            this.jtaCList.setVisible(false);
        } else if (this.jcbConstruction.getSelectedIndex() == 2) {
            this.jtf0.setVisible(true);
            this.jtf1.setVisible(false);
            this.jl0.setText("Area (m2)");
            this.jl0.setVisible(true);
            this.jl1.setText("");
            this.jl1.setVisible(false);
            this.jButtonOk.setVisible(true);
            this.jtaCList.setText("");
            this.jtaCList.setVisible(false);
        }
    }//GEN-LAST:event_jcbConstructionActionPerformed

    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkActionPerformed
        if (this.jcbConstruction.getSelectedIndex() == 1) {
            Stair s = new Stair(Integer.parseInt(this.jtf0.getText()), Integer.parseInt(this.jtf1.getText()));
            this.jtaCList.append(s.toString());
            this.jtaCList.setVisible(true);
        } else if (this.jcbConstruction.getSelectedIndex() == 2) {
            Foundation s = new Foundation(Integer.parseInt(this.jtf0.getText()));
            this.jtaCList.append(s.toString());
            this.jtaCList.setVisible(true);
        }
    }//GEN-LAST:event_jButtonOkActionPerformed


